# Numericos
entero = 3
print(entero)
print(type(entero))
decimal = 2.8
print(decimal)
print(type(decimal))

# Textos
texto = 'Esto es una cadena de texto'
print(texto)
print(type(texto))

# Booleanos
booleano_verdadero = True
booleano_falso = False
print(booleano_verdadero)
print(type(booleano_verdadero))
print(booleano_falso)
print(type(booleano_falso))

# Lista
lista = [2, 4, 6, 2, 7, "Hola", True]
print(lista)
print(type(lista))
lista.append(5)
print(lista)

# Tupla
tupla = (1, 3, 4, 7, 9, 'hola')
print(tupla)
print(type(tupla))
