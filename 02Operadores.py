# Operadores aritmeticos
# + - * / ** % //
# asignamos valor a variables
num1 = 6
num2 = 4
print(num1+num2)
print("*"*56)  # crea division entre impresiones, caracter 56 veces
print(num1-num2)
print("*"*56)
print(num1*num2)
print("*"*56)
print(num1/num2)
print("*"*56)
# Resto de la division
print(num1 % num2)
print("*"*56)
# Parte entera
print(num1//num2)
print("*"*56)
# elevar, potencia
print(num1**num2)
print("*"*56)

# Operadores logicos o de comparacion
print(num1 == num2)
print("*"*10)
print(num1 != num2)
print("*"*10)
print(num1 < num2)
print("*"*10)
print(num1 <= num2)
print("*"*10)
print(num1 > num2)
print("*"*10)
print(num1 >= num2)
print("*"*10)

# Operadores booleanos
# Se tienen que cumplir los 2
print(num1 and num2)
print(1 == 0 and 1 != 2)
print("*"*10)
# Solo necesita que 1 sea verdadero
print(num1 or num2)
print(1 < 2 or 1 > 9)
print("*"*10)
# Negacion
print(not num1)
print(not 1 == 4 or 1 < 5)
print("*"*10)
